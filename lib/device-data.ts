// Get models for a specific device type and brand
export function getModels(deviceType: DeviceType, brand: string): string[] {
  const lowerBrand = brand.toLowerCase()

  // Handle special cases like "HPE/Aruba" which might be stored as "hpe/aruba"
  const brandKey = Object.keys(brandModels[deviceType] || {}).find((key) => key.toLowerCase() === lowerBrand)

  return brandKey ? brandModels[deviceType][brandKey] : []
}
