// Helper function to get device type label
export function getDeviceTypeLabel(type: DeviceType): string {
  switch (type) {
    case "laptop":
      return "Laptop"
    case "desktop":
      return "Desktop"
    case "server":
      return "Server"
    case "workstation":
      return "Workstation"
    case "network":
      return "Network Device"
    case "printer":
      return "Printer/Copier"
    case "monitor":
      return "Monitor"
    default:
      return "Device"
  }
}

